<h2>

Pada suatu hari si kancil nampak ngantuk sekali. Matanya serasa berat sekali untuk dibuka. “Aaa....rrrrgh”, <br>
si kancil nampak sesekali menguap. Karena hari itu cukup cerah, si kancil merasa rugi jika menyia-nyiakannya.<br>
 Ia mulai berjalan-jalan menelusuri hutan untuk mengusir rasa kantuknya. Sampai di atas sebuah bukit, si Kancil <br>
 berteriak dengan sombongnya, “Wahai penduduk hutan, akulah hewan yang paling cerdas, cerdik dan pintar di hutan ini. <br>
 Tidak ada yang bisa menandingi kecerdasan dan kepintaranku”.<br>
<br>
Sambil membusungkan dadanya, si Kancil pun mulai berjalan menuruni bukit. Ketika sampai di sungai, ia bertemu <br>
dengan seekor siput. “Hai kancil !”, sapa si siput. “Kenapa kamu teriak-teriak? Apakah kamu sedang <br>
bergembira?”, tanya si siput. “Tidak, aku hanya ingin memberitahukan pada semua penghuni hutan kalau aku <br>
ini hewan yang paling cerdas, cerdik dan pintar”, jawab si kancil dengan sombongnya.<br>
<br>
Siput“Sombong sekali kamu Kancil, akulah hewan yang paling cerdik di hutan ini”, kata si Siput. “Hahahaha.......,<br>
 mana mungkin” ledek Kancil. “Untuk membuktikannya, bagaimana kalau besok pagi kita lomba lari?”, tantang si Siput.<br>
  “Baiklah, aku terima tantanganmu”, jawab si Kancil. Akhirnya mereka berdua setuju untuk mengadakan perlombaan <br>
  lari besok pagi.<br>
<br>
Setelah si Kancil pergi, si siput segera mengumpulkan teman-temannya. Ia meminta tolong agar teman-temannya <br>
berbaris dan bersembunyi di jalur perlombaan, dan menjawab kalau si kancil memanggil.<br>
<br>
Akhirnya hari yang dinanti sudah tiba, kancil dan siput pun sudah siap untuk lomba lari. “Apakah kau sudah <br>
siap untuk berlomba lari denganku”, tanya si kancil. “Tentu saja sudah, dan aku pasti menang”, jawab si siput. <br>
Kemudian si siput mempersilahkan kancil untuk berlari dahulu dan memanggilnya untuk memastikan sudah sampai mana si siput.<br>
<br>
Kancil berjalan dengan santai, dan merasa yakin kalau dia akan menang. Setelah beberapa langkah, si kancil <br>
mencoba untuk memanggil si siput. “Siput....sudah sampai mana kamu?”, teriak si kancil. “Aku ada di depanmu!”, <br>
teriak si siput. Kancil terheran-heran, dan segera mempercepat langkahnya. Kemudian ia memanggil si siput lagi, <br>
dan si siput menjawab dengan kata yang sama.”Aku ada didepanmu!”<br>
<br>
Akhirnya si kancil berlari, tetapi tiap ia panggil si siput, ia selalu muncul dan berkata kalau dia ada depan <br>
kancil. Keringatnya bercucuran, kakinya terasa lemas dan nafasnya tersengal-sengal. Kancil berlari terus, sampai <br>
akhirnya dia melihat garis finish. Wajah kancil sangat gembira sekali, karena waktu dia memanggil siput, sudah<br> 
tidak ada jawaban lagi. Kancil merasa bahwa dialah pemenang dari perlombaan lari itu.<br>
<br>
Betapa terkejutnya si kancil, karena dia melihat si siput sudah duduk di batu dekat garis finish. “Hai kancil,<br> 
kenapa kamu lama sekali? Aku sudah sampai dari tadi!”, teriak si siput. Dengan menundukkan kepala, si kancil <br>
menghampiri si siput dan mengakui kekalahannya. “Makanya jangan sombong, kamu memang cerdik dan pandai, <br>
tetapi kamu bukanlah yang terpandai dan cerdik”, kata si siput. “Iya, maafkan aku siput, aku tidak akan sombong <br>
lagi”, kata si kancil.<br>
