<center><h1>Raja yang Bodoh</h1></center>
<hr>

<center>
<h2>

Dahulu kala, ada seorang raja yang pesolek dan sangat suka mengenakan baju-baju baru. Dia banyak menghabiskan waktu hanya <br>
untuk memandangi dirinya sendiri di cermin, dan selalu ingin mengenakan baju-baju baru di pagi, siang dan malam hari!! <br>
<br>
Pada suatu hari, datanglah dua orang penipu yang menyamar sebagai pembuat baju yang hebat. Mereka mengaku bahwa mereka <br>
pandai menenun dan membuat baju dengan kualitas yang sangat bagus, sampai-sampai kain yang mereka pakai untuk membuat <br>
baju tidak akan terlihat, kecuali oleh orang-orang pintar. Ketika raja mendengar hal itu, dia sangat tertarik. “Itu <br>
bagus, aku bisa tahu siapa saja yang bodoh dan siapa saja yang pintar di kerajaan ini.” Pikirnya. Raja segera <br>
memerintah kedua orang itu untuk membuatkan baju baru untuk dirinya, menggunakan bahan kain istimewa itu. Mereka diberi <br>
sebuah ruangan khusus di istana, beserta benang-benang emas yang mereka minta. Kedua penipu itu menyembunyikan benang-<br>
benang emas yang mereka terima, kemudian berpura-pura sedang bekerja keras untuk membuat sebuah baju. <br>
<br>
Beberapa hari kemudian, raja yang tidak sabar mengutus menteri nya untuk menengok baju istimewa yang sedang dibuat itu. <br>
Ketika menteri mengunjungi para penipu yang menyamar itu, ia pun kebingungan. “Aku tidak melihat apa pun disini” pikirnya. <br>
Akan tetapi menteri itu tidak mau mengakuinya karena tidak ingin dianggap bodoh. Maka ia pun memuji kedua penipu itu dan <br>
mengatakan bahwa baju yang mereka buat sangat indah. Setelah menteri keluar dari ruangan itu, kedua penipu tertawa terbahak-bahak. <br>
<br>
Tak lama kemudian sang raja datang untuk melihat sendiri. Dia berusaha melihat keseluruh ruangan, tapi ia tidak melihat<br>
 apa pun. Namun, karena tidak ingin dianggap bodoh, raja pun berpura-pura bisa melihat baju yang istimewa itu dan berkata,<br>
  “Baju yang sangat indah, aku tidak sabar ingin segera memakainya” <br>
<br>
Keesokan harinya adalah hari dimana sang raja akan mengenakan baju barunya pada acara pawai keliling kota. Kedua penipu <br>
yang menyamar telah berpamitan dan pergi dengan alasan akan membuatkan baju untuk raja dari kerajaan-kerajaan lain. Tentu <br>
saja, mereka tidak lupa membawa benang-benang emas yang telah mereka sembunyikan, beserta uang emas upah membuat baju. <br>
<br>
Saat raja memakai baju barunya, ia tetap saja tidak bisa melihat baju itu, dan ia merasa kedinginan. Tapi karena tidak ingin<br>
 dibilang bodoh, raja pun berputar-putar di depan cermin dan mengagumi baju barunya, walaupun ia tidak melihat apa-apa.<br>
  Semua pegawai kerajaan juga mengatakan bahwa baju baru itu sangat indah, karena mereka juga tidak ingin dianggap bodoh.<br>
<br>
Seluruh rakyat telah mendengat bahwa raja akan mengenakan baju baru sang spesial hari itu. Saat sang raja muncul, semuanya <br>
terkejut. Akan tetapi mereka juga telah mendengar kabar bahwa baju baru yang spesial itu hanya dapat dilihat oleh orang <br>
yang pintar saja, dan karena mereka tidak ingin dianggap bodoh, mereka pun berseru-seru memuji sang raja. Mendadak terdengar <br>
suara anak kecil berteriak, “tetapi, dia kan tidak pakai baju, sang raja telanjang!” Semua terdiam. Raja pun menyadari bahwa <br>
anak kecil itu berkata jujur, dan dengan terburu-buru ia berjalan kembali ke istana.<br>

</h2>
