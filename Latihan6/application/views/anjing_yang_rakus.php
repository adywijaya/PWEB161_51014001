<center><h1>Anjing yang rakus</h1></center>
<hr>

<center>
<h2>

Adalah seekor anjing  mencuri sepotong  tulang yag besar di warung. Ia berlari kencang sekali sehingga tidak <br>
terkejar si tukang daging. Ia berlari ke ladang sambil membawa tulang di moncongnya. Ia ingin makan semuanya sendirian.<br>
<br>
Anjing itu melewati sebuah sungai kecil. Ada sebuah jembatan sempit di atasnya. Ia berjalan di jembatan itu <br>
sambil melihat ke air. Ia melihat bayangannya sendiri di dalam air. Ia berpikir ada anjing lain dengan tulang <br>
di mulutnya. Anjing yang rakus itu berpikir tulang yang di mulut anjing itu lebih besar dari pada yang ia bawa. <br>
<br>
Ia meloncat ke air untuk merebut tulang yang lebih besar dari anjing yang ia lihat tadi. Ia meloncat dengan <br>
sangat kuat sehingga tulang di mulutnya terlepas. Ia mencari di mana-mana tetapi tidak menemukan anjing yang<br>
 lain. Bayangan tadi telah hilang.<br>
<br>
Anjing yang bodoh itu pulang kelaparan dan kedinginan. Ia kehilangan tulang yang ia curi dari tukang daging <br>
dan tidak mendapatkan apa pun karena ia terlalu rakus.<br>

