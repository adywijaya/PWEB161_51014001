<center><h1>Kancil dan Buaya</h1></center>
<hr>

<center>
<h2>
Pada suatu hari, si Kancil yang cerdik sedang berjalan-jalan di hutan. Karena merasa haus, Kancil pun mencari <br>
sungai agar ia bisa minum. Ketika sedang minum, Kancil melihat kalau di seberang sungai ada banyak pohon ketimun, <br>
buah yang sangat digemarinya. Tapi sayangnya, arus sungai terlalu deras. Kancil tahu bahwa ia tidak mungkin <br>
berjalan atau berenang menyeberangi sungai itu. <br>
<br>
Kancil pun berpikir keras. Ia mencari cara untuk menyeberangi sungai yang arusnya deras itu. Tiba-tiba ada <br>
sekelompok buaya yang berenang melewatinya. Kancil pun mendapatkan ide yang cemerlang. "Hai buaya buaya!" <br>
teriak Kancil dengan lantang. "Aku punya makanan untuk kalian!" lanjut Kancil. Para buaya itu pun berhenti dan <br>
salah satunya ke pinggir sungai mendekati Kancil. "Hmm, kamu benar, kamu lah makanan kami!" katanya. "Eit tunggu<br>
 dulu," kata Kancil. "Ini aku punya makanan yang sangat banyak, bahkan masih terlalu banyak untuk kalian semua," <br>
 lanjutnya. "Coba panggil teman-teman kalian yang lainnya, dan akan aku tunjukkan makanan itu," kata Kancil.<br>
<br>
Buaya tadi lalu memanggil teman-temannya yang lain, dan semuanya berkumpul di sungai itu. Karena banyaknya <br>
jumlah buaya yang berkumpul, sungai yang lebar dan airnya deras itu sampai hampir penuh. "Oke, sekarang aku<br>
 harus menghitung jumlah kalian dulu supaya semuanya kebagian!" kata Kancil. Ia pun lalu melompat dari <br>
 punggung satu buaya ke punggung buaya yang lainnya, sambil menghitung. "Satu, dua, tiga, empat, lima, enam," dan <br>
 seterusnya, sampai ia tiba di seberang sungai.<br>
Sambil berlari pergi, Kancil pun berteriak, "terima kasih buaya-buaya, kalian sudah membantu aku menyeberang <br>
sungai!" Beberapa buaya marah karena sudah dibohongi, dan mencoba mengejarnya. Tapi mereka gagal karena Kancil <br>
sangat lincah dan cepat. 
