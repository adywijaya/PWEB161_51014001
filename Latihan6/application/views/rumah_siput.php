dan kedinginan, siput masuk ke dalam cangkang itu. Siput merasa hangat dan nyaman lalu tidur bergelung di dalamnya.<br> 
<br> 
Ketika pagi datang, siput menyadari telah menemukan rumah yang terbaik baginya. Cangkang ini sangat cocok <br> 
untuknya. Aku tidak perlu lagi cepat-cepat pulang jika hujan turun, aku tidak akan kepanasan lagi, tidak <br> 
ada yang akan menggangguku. Aku akan membawa rumah ini bersamaku kemanapun aku pergi.<br> 
