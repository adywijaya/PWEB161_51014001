daun-daun itu tidak bisa lagi menghalangi air hujan yang jatuh. Siput menjadi basah dan kedinginan terkena air hujan.<br> 
<br> 
Kemudian siput pindah ke dalam lubang yang ada di batang pohon, Jika hari panas, siput terlindung dengan baik, <br> 
bahkan jika hujan turun, siput tidak akan basah dan kedinginan. Sepertinya aku menemukan rumah yang cocok <br> 
untukku, gumam siput dalam hati.<br> 
