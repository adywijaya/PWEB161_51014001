<h2>

Dahulu, hiduplah seekor monyet dan seekor kura-kura. Mereka adalah sahabat yang akrab. Tak pernah terpisahkan <br>
oleh jarak dan waktu. Setiap pagi, mereka selalu jalan bersama, makan bersama, semua selalu bersama. Suatu hari,<br> 
mereka menemukan beberapa biji pisang. "Hei, Ra. Gimana kalau kita tanam biji pisang ini? Siapa tahu berbuah,<br>
" kata monyet. "Ya, ya. Ayo kita tanam biji pisang ini," kata kura-kura semangat.<br>
<br>
Mereka pun kembali ke rumah mereka masing-masing. Di rumah monyet, ia menanam biji pisang itu di halaman <br>
rumahnya. Tapi, monyet tidak rajin merawatnya. Terkadang seminggu sekali. Bahkan pernah dalam seminggu tidak <br>
dirawat sedikitpun. Maka, pohon pisang monyet masih kecil sekali. Sementara itu, kura-kura menanam pohon <br>
pisang itu dengan rajin. Dia selalu menyiramnya setiap hari. Akhirnya pohon pisang kura-kura sudah besar dan berbuah.<br>
<br>
Monyet dan kura-kuraSuatu hari, monyet pergi ke rumah kura-kura. Dilihatnya pisang yang sudah besar dan matang. <br>
Kebetulan juga kura-kura meminta tolong pada monyet. "Sahabat baikku, maukah kau petikkan untukku pisang itu? <br>
Tenang saja, kau juga akan kubagi," kata kura-kura. Dalam hati monyet, monyet senang. Tapi, ada suatu niat jahat.<br>
 Dia akan memanjat pohon lalu memakan semua pisang kura-kura tanpa memberinya. "Baiklah, aku akan mengambilnya,<br>
 " kata monyet. Monyet lalu memanjat pohon itu.<br>
Begitu sampai di atas, monyet langsung memakan pisang yang ada di pohon itu. Kura-kura kaget dan marah. "Hei sahabatku! <br>
Mengapa kau makan pisangku?!" tanya kura-kura marah. Si monyet tak menghiraukannya lagi. Dimakannya semua <br>
pisang itu sampai kenyang. Tapi salah satu dari dahan pisang itu retak. Akhirnya dahan itu jatuh bersama monyet. <br>
Si monyet itu pun meringis kesakitan. Tulang punggungnya patah.<br>
