<h2>

Dahulu kala, ada sebuah taman yang sangat luas dan cantik, milik seorang raksasa. Taman itu sangat <br>
indah dengan rumput yang hijau dan lembut, bunga-bunga yang cantik, dan puluhan pohon yang berbuah<br>
 lebat. Setiap siang, anak-anak masuk ke dalam taman itu untuk bermain dan mendengarkan burung-burung <br>
 berkicau merdu dari pohon-pohon.  <br>
<br>
Raksasa sedang pergi selama 5 tahun mengunjungi keluarganya di negeri lain. Sekarang, dia kembali ke <br>
rumahnya, sebuah rumah yang sangat besar dengan taman di depannya.  Saat tiba di taman, ia melihat <br>
anak-anak sedang bermain disana. Raksasa lalu memarahi mereka, “Apa yang kalian lakukan disini? Pergi!<br>
 Ini taman milikku!” Anak-anak yang ketakutan berlari meninggalkan taman itu.  <br>
<br>
Karena tidak ingin ada orang lain yang ikut menikmati keindahan tamannya lagi, raksasa lalu membangun <br>
tembok yang tinggi mengelilingi taman itu, dan memadang tulisan “Yang masuk tanpa ijin akan dihukum!” <br>
Anak-anak kehilangan taman itu. Sesekali mereka memanjat dan melongok melewati tembok yang tinggi,<br>
 memandangi taman itu dan dengan sedihnya membicarakan permainan-permainan yang dulu mereka lakukan disana. <br>
<br>
Hari demi hari berlalu. Bunga-bunga di taman itu tidak lagi bermekaran. Burung-burung tidak lagi berkicau <br>
dan pohon-pohon berhenti berbuah. Rumput dan daun-daun yang dulunya subur dan hijau kini menjadi kering <br>
dan berwarna coklat. Raksasa tidak mengerti mengapa taman miliknya menjadi tidak indah lagi. <br>
<br>
Pada suatu pagi, raksasa mendengar suara musik yang mengalun. Ternyata itu adalah suara kicauan burung <br>
di luar jendelanya. Sudah lama sekali sejak terakhir kali ia mendengar kicauan burung yang indah seperti<br>
 itu. Raksasa mendekat ke jendela dan mendengarkan kicauan burung itu dengan sedih. “Apa yang terjadi <br>
 dengan tamanku? Aku berharap tamanku bisa menjadi indah seperti dulu, dengan burung-burung yang berkicau<br>
  merdu seperti kamu.” kata raksasa kepada burung itu.  Burung itu terbang mendekati raksasa dan berkata <br>
  “Tamanmu tidak akan sama lagi tanpa kehadiran anak-anak itu. Tamanmu merindukan gelak tawa dan suara <br>
  anak-anak yang riang. Pohon, bunga-bunga, rumput, dan kami para burung menginginkan kehadiran anak-anak<br>
   yang menjadikan tempat ini kembali penuh keceriaan.” <br>
<br>
Raksasa menyadari kesalahannya. Selama ini ia terlalu egois, dan akibatnya ia hidup sendirian dan merasa <br>
kesepian. Raksasa pun mengambil palu besar dan menghancurkan tembok yang mengelilingi tamannya. Dibuangnya <br>
tulisan peringatan yang dipasangnya dulu, dan dipanggilnya anak-anak untuk bermain di taman.  Awalnya anak-anak <br>
merasa takut. Akan tetapi ketika mereka melihat wajah raksasa yang sekarang menjadi ramah, mereka mengikutinya <br>
ke taman untuk bermain disana. Lagipula, anak-anak itu juga rindu bermain di taman itu. <br>
<br>
Taman milik raksasa itu pun kembali penuh dengan anak-anak yang bermain gembira. Bunga-bunga pun kembali <br>
bermekaran diantara rerumputan yang hijau. Daun-daun dan buah-buahan memenuhi pohon-pohon, beserta burung-burung <br>
yang berkicau dengan merdu. Raksasa berkata kepada anak-anak, “Sekarang, tamanku adalah taman milik kalian juga.<br>
” Sekarang raksasa tidak hanya memiliki sebuah taman yang indah, tetapi ia juga memiliki banyak teman-teman kecil yang ceria.  <br>

