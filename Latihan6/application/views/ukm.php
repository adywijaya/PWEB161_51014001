<h2>
Visi<br>
Menjadi UKM yang mampu mewadahi dan mengembangkan minat, bakat dan kemampuan khusus mahasiswa di bidang bulutangkis dalam rangka mewujudkan 
mahasiswa yang sehat jasmani dan rohani sehingga mampu berprestasi baik di bidang akademik (pada umumnya) dan 
di bidang bulutangkis (khususnya) untuk diri pribadi, institusi, bangsa dan negara.<br>
<br>
Misi<br>
I<br>
Sebagai UKM yang mampu membawa dan menjaga nama baik STIMK Kharisma di lingkup nasional atau bahkan internasional.<br>

II<br>
Sebagai wadah pembinaan dan pelatihan yang baik dalam hal bulutangkis dan segala aspeknya bagi mahasiswa STIMK Kharisma.<br>

III<br>
Menyelenggarakan atau mengikuti berbagai kegiatan yang konstruktif (ke dalam dan ke luar) yang bersifat “kompetitif”.<br>
